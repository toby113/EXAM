const subjectRows = db.prepare(`SELECT id, code, grade_level, name FROM subjects`).all();
const subjectMap = new Map(subjectRows.map(s => [`${s.grade_level}:${s.code}`, s]));
const existsStmt = db.prepare(`SELECT id FROM questions WHERE grade_level = ? AND subject_id = ? AND content = ?`);
const insertStmt = db.prepare(`
  INSERT INTO questions (
    subject_id, type, difficulty, content,
    option_a, option_b, option_c, option_d,
    answer, explanation, source, tags, grade_level
  ) VALUES (?, 'true_false', ?, ?, NULL, NULL, NULL, NULL, ?, ?, ?, ?, ?)
`);

const run = db.transaction(() => {
  const summary = {};
  let inserted = 0;
  let skipped = 0;

  for (const [gradeLevel, sets] of Object.entries(gradePlans)) {
    summary[gradeLevel] = 0;
    for (const [code, items] of sets) {
      const subject = subjectMap.get(`${gradeLevel}:${code}`);
      if (!subject) throw new Error(`找不到科目：${gradeLevel}:${code}`);
      items.forEach((item, index) => {
        const [content, answer, explanation, tags] = item;
        const fullContent = `【是非題】${content}`;
        if (existsStmt.get(gradeLevel, subject.id, fullContent)) {
          skipped++;
          return;
        }
        const difficulty = 1 + (index % 3);
        insertStmt.run(subject.id, difficulty, fullContent, answer, explanation, SOURCE, tags, gradeLevel);
        inserted++;
        summary[gradeLevel]++;
      });
    }
  }

  return { inserted, skipped, summary };
});

const result = run();
