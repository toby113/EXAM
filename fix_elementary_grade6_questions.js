const updateStmt = db.prepare(`
  UPDATE questions
  SET subject_id = ?,
      type = ?,
      difficulty = ?,
      content = ?,
      option_a = ?,
      option_b = ?,
      option_c = ?,
      option_d = ?,
      answer = ?,
      explanation = ?,
      tags = ?,
      grade_level = 'elementary_6',
      updated_at = datetime('now','localtime')
  WHERE id = ? AND grade_level = 'elementary_6'
`);

const applyUpdates = db.transaction((items) => {
  let changed = 0;
  for (const item of items) {
    const result = updateStmt.run(
      item.subject_id,
      item.type,
      item.difficulty,
      item.content,
      item.option_a,
      item.option_b,
      item.option_c,
      item.option_d,
      item.answer,
      item.explanation,
      item.tags,
      item.id
    );
    changed += result.changes;
  }
  return changed;
});

const changed = applyUpdates(updates);
console.log(`Updated ${changed} elementary_6 questions.`);
